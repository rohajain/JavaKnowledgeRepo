	public static void paintCustomBitMap(File binaryImage, char background, char forground) throws IOException
	{
		byte[] binaryImageBytes = Files.readAllBytes(Paths.get(binaryImage.getAbsolutePath()));
		paintCustomBitMap(new String(binaryImageBytes), background, forground);
	}

