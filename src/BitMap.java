	public static void paintChar(int fontHeight, int fontWidth, String pixel, String background, String text) {
		char rowBits;

		int pixelIndex = 0;

		for (byte row = 0; row < 16; row++) {
			for (byte fh = 0; fh < fontHeight; fh++) {
				for (char character : text.toCharArray()) {
					// ascii code mapping, characters between 0 to 32 in ascii
					// are not available in asc2_1608[]
					character -= 32;
					rowBits = asc2_1608[character * 16 + row];
					for (byte col = 0; col < 8; col++)
						for (byte fw = 0; fw < fontWidth; fw++) {
							if (((rowBits >> col) & 0x0001) == 0x0001) {
								// System.out.print(col%2==0?1:0);
								if (pixelIndex >= pixel.length())
									pixelIndex = 0;
								System.out.print(pixel.charAt(pixelIndex++));
							} else
								System.out.print(background);
						}
				}
				pixelIndex = 0;
				System.out.print("\n");
			}
		}
	}

